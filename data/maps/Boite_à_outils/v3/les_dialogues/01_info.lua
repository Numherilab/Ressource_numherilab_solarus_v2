Placer l'entité PNJ sur la carte : 

Double-cliquer sur l'entité pour ouvrir la fenêtre de configuration.
1  Dans la fenêtre de configuration, sélectionnez le sprite de votre PNJ que vous souhaitez utiliser dans le dossier npc.
2 Choisissez le sous-type entre "quelqu'un" une personne) ou "quelque chose" (un objet ou un élément interactif).
3  Dans les options du PNJ, cliquez sur le bouton "Afficher un dialogue".
saisissez le titre du dialogue que vous souhaitez créer. Ce titre sera utilisé pour identifier le dialogue dans votre projet. Copier le titre pour la suite ...
